package calculator;

public class CalculatorCMD {

    static public int subtraction(int a, int b) {
        return a - b;
    }

    static public int summation(int a, int b) {
        return a + b;
    }

    static public int division(int a, int b) {
        return a / b;
    }

    static public int multiplication(int a, int b) {
        return a * b;
    }

    static public int degree(int a) {
        return a * a;

    }
    static public int root(int a) {
        return a;
    }
}